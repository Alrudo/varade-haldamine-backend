CREATE TABLE IF NOT EXISTS asset (
    id VARCHAR (50) PRIMARY KEY,
    name VARCHAR (50),
    subclass VARCHAR (50),
    active VARCHAR (50),
    user_id VARCHAR (50),
    owner_id VARCHAR (50),
    accured VARCHAR (50),
    expiration_date VARCHAR (50),
    delicate_condition VARCHAR (50),
    created_at VARCHAR (50),
    modified_at VARCHAR (50)
);